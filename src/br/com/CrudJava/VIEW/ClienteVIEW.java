    public void limpaCampos(){
    
     txt_cod.setText("");
     txt_nome.setText("");
     txt_cep.setText("");
     txt_cpf.setText("");
     txt_endereco.setText("");
     cmb_cidade.setSelectedItem(null);
     cmb_estado.setSelectedItem(null);
     cmb_sexo.setSelectedItem(null);
        
    }
    
    public void opcao(){
    
    switch(opcao){
    
        case "alterar":
            
            AlterarCliente();
            opcao = "";
            break;
            
        case "cadastrar":
            
            CadastrarCliente();
               opcao = "";
            break;
    
    } 
    
    }
    
    
 
     
      public void AlterarCliente(){
          
    ClienteCTR clienteCtr = new ClienteCTR();
    CidadeMODEL cidadeModel = new CidadeMODEL();
    SexoMODEL sexoModel = new SexoMODEL();
    
     Integer cod = Integer.parseInt(txt_cod.getText());
    
     sexoModel.setCod(sexoList.get(cmb_sexo.getSelectedIndex()).getCod());
    
     cidadeModel.setCod_cidade(cidadeList.get(cmb_cidade.getSelectedIndex()).getCod_cidade());
     
     clienteCtr.SetFk_Sexo_Cliente(sexoModel);
     clienteCtr.SetFk_Cidade_Cliente(cidadeModel);
     clienteCtr.SetEndereco_Cliente(txt_endereco.getText());
     clienteCtr.SetCPF_Cliente(txt_cpf.getText());
     clienteCtr.SetCep_Cliente(txt_cep.getText());
     clienteCtr.SetNome_Cliente(txt_nome.getText());
     
    clienteCtr.AlterarCliente(cod);
 
    }
    
      public void CadastrarCliente(){
          
     ClienteCTR clienteCtr = new ClienteCTR();
    CidadeMODEL cidadeModel = new CidadeMODEL();
    SexoMODEL sexoModel = new SexoMODEL();
          
       sexoModel.setCod(sexoList.get(cmb_sexo.getSelectedIndex()).getCod());
    
     cidadeModel.setCod_cidade(cidadeList.get(cmb_cidade.getSelectedIndex()).getCod_cidade());
     
     clienteCtr.SetFk_Sexo_Cliente(sexoModel);
     clienteCtr.SetFk_Cidade_Cliente(cidadeModel);
     clienteCtr.SetEndereco_Cliente(txt_endereco.getText());
     clienteCtr.SetCPF_Cliente(txt_cpf.getText());
     clienteCtr.SetCep_Cliente(txt_cep.getText());
     clienteCtr.SetNome_Cliente(txt_nome.getText());
     
    clienteCtr.CadastrarCliente();
    
    
    }
     public void ExcluirCliente(){
         
      ClienteCTR clienteCtr = new ClienteCTR();
    
       Integer cod = Integer.parseInt(txt_cod.getText());
       
     clienteCtr.DeletarCliente(cod);

     }
     
     public void CarregaComboEstado(){
         
      
        
      cmb_estado.removeAllItems();
     ResultSet rs_estado;
     EstadoCTR estadoCtr = new EstadoCTR();
    rs_estado = estadoCtr.ListaEstado();
    
        try {
          
            while(rs_estado.next()){
            EstadoMODEL estadoModel = new EstadoMODEL();
            
            estadoModel.setCod(rs_estado.getInt("cod_estado"));
            estadoModel.setNome(rs_estado.getString("nome_estado"));
            estadoModel.setSigla(rs_estado.getString("sigla_estado"));
            
            estadoList.add(estadoModel);
      
               cmb_estado.addItem(estadoModel.getSigla());
          
            }
                
                } catch (SQLException ex) {
                    
            Logger.getLogger(ClienteVIEW.class.getName()).log(Level.SEVERE, null, ex);
       
                }
   
     }
     
