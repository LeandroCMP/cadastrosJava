    public void carregaEstado(){
    
        EstadoCTR ctr = new EstadoCTR();
        
        rs=ctr.ListaEstado();
    
    
    
    }
    
  
    public void limpaCampos(){
    
    txt_cod.setText("");
    txt_estado.setText("");
    txt_sigla.setText("");
        
    }
    
    public void opcao(){
    
    switch(opcao){
    
        case "alterar":
            
            alterarEstado();
            opcao = "";
            break;
            
        case "cadastrar":
            
            CadastrarEstado();
               opcao = "";
            break;
    
    } 
    }
    
    public void alterarEstado(){
     
    EstadoCTR ctr = new EstadoCTR();
    
     Integer cod = Integer.parseInt(txt_cod.getText());
     ctr.SetNome(txt_estado.getText());
     ctr.SetSigla(txt_sigla.getText());
    
    ctr.AlterarEstado (cod);
    carregaEstado();
    }
    
      public void CadastrarEstado(){
    
          EstadoCTR ctr = new EstadoCTR();
          
     ctr.SetNome(txt_estado.getText());
     ctr.SetSigla(txt_sigla.getText());
    
    
    
    ctr.InserirEstado();
    carregaEstado();
    
    }
     public void ExcluirEstado(){
      EstadoCTR ctr = new EstadoCTR();
       Integer cod = Integer.parseInt(txt_cod.getText());
       
     ctr.ExcluirEstado(cod);
     carregaEstado();
     }
     
  
     
     
     

